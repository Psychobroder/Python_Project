# Operadores Condicionales o de comparación

# Simbolo   Significado         Ejemplo     Resultado

# ==        Igual que           5 == 7      Falso
# !=        distinto que        5 != 7      Verdadero
# <         Menor que           5 < 7       Verdadero
# >         Mayor que           5 > 7       Falso
# <=        Menor o igual que   7 <= 7      Verdadero
# >=        MAyor o igual que   5 >= 5      Verdadero

